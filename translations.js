// ── FUNÇÕES ────────────────────────────────────────────────────────────────────
function applyTranslations(lang) {
  var dict = translations[lang] || translations['pt'];
  document.querySelectorAll('[data-lang-key]').forEach(function(el) {
    var key = el.getAttribute('data-lang-key');
    if (dict[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = dict[key];
      } else {
        el.innerHTML = dict[key];
      }
    }
  });
  var langMap = { pt: 'pt-BR', en: 'en', es: 'es', pl: 'pl' };
  document.documentElement.lang = langMap[lang] || 'pt-BR';
}

/**
 * Detecta o idioma preferido do visitante.
 * Ordem de prioridade:
 *   1. Escolha salva (localStorage) — respeita preferência manual
 *   2. navigator.language do browser
 *   3. Fallback: 'pt'
 */
function detectBrowserLanguage() {
  var supported = ['pt', 'en', 'es', 'pl'];
  var browserLang = (navigator.language || navigator.userLanguage || 'pt').toLowerCase();
  // pt-BR, pt-PT → pt | en-US, en-GB → en | es-* → es | pl-* → pl
  var prefix = browserLang.split('-')[0];
  return supported.indexOf(prefix) !== -1 ? prefix : 'pt';
}

function initPageLanguage() {
  // Se o usuário nunca escolheu manualmente, usa o idioma do browser
  var stored = localStorage.getItem('sms-lang');
  var lang = stored || detectBrowserLanguage();
  applyTranslations(lang);
  var flags = { pt: '🇧🇷', en: '🇺🇸', es: '🇪🇸', pl: '🇵🇱' };

  // Suporte ao switcher padrão do index.html (ids: currentLang, .flag, .lang-code)
  var btn = document.getElementById('currentLang');
  if (btn) {
    var flagEl = btn.querySelector('.flag');
    var codeEl = btn.querySelector('.lang-code');
    if (flagEl) flagEl.textContent = flags[lang] || '🇧🇷';
    if (codeEl) codeEl.textContent = lang.toUpperCase();
  }

  // Suporte ao switcher das páginas internas (ids: smsCurrentLang, smsFlag, smsCode, smsDropdown)
  var smsBtn = document.getElementById('smsCurrentLang');
  if (smsBtn) {
    var smsFlagEl = document.getElementById('smsFlag');
    var smsCodeEl = document.getElementById('smsCode');
    if (smsFlagEl) smsFlagEl.textContent = flags[lang] || '🇧🇷';
    if (smsCodeEl) smsCodeEl.textContent = lang.toUpperCase();

    // Inicializa o dropdown se ainda não foi feito
    if (!smsBtn._smsInitialized) {
      smsBtn._smsInitialized = true;
      var dropdown = document.getElementById('smsDropdown');

      smsBtn.addEventListener('click', function(e) {
        e.stopPropagation();
        dropdown.style.display = dropdown.style.display === 'none' ? 'block' : 'none';
      });

      document.querySelectorAll('.sms-lang-opt').forEach(function(opt) {
        opt.addEventListener('click', function() {
          var selectedLang = opt.getAttribute('data-lang');
          localStorage.setItem('sms-lang', selectedLang);
          dropdown.style.display = 'none';
          applyTranslations(selectedLang);
          var f = document.getElementById('smsFlag');
          var c = document.getElementById('smsCode');
          if (f) f.textContent = flags[selectedLang] || '🇧🇷';
          if (c) c.textContent = selectedLang.toUpperCase();
          // Atualizar placeholder de inputs
          document.querySelectorAll('[data-lang-key-placeholder]').forEach(function(inp) {
            var k = inp.getAttribute('data-lang-key-placeholder');
            var dict = translations[selectedLang] || translations['pt'];
            if (dict[k]) inp.placeholder = dict[k];
          });
        });
      });

      document.addEventListener('click', function() {
        dropdown.style.display = 'none';
      });
    }
  }
}

window.applyTranslations = applyTranslations;
window.initPageLanguage = initPageLanguage;

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initPageLanguage);
} else {
  initPageLanguage();
}
