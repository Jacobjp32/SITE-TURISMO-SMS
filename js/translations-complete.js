// Função para aplicar traduções completas
function applyCompleteTranslations(lang) {
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.dataset.translate;
        if (translationsComplete[key] && translationsComplete[key][lang]) {
            el.innerHTML = translationsComplete[key][lang];
        }
    });
}

// Exportar
window.translationsComplete = translationsComplete;
window.applyCompleteTranslations = applyCompleteTranslations;
